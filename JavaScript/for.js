/*
For sirve para recorrer
*/

// creciente
for(i=1; i<=5; i++){
    console.log(i);
}


// decreciente
for(i=5; i>=1; i--){
    console.log(i);
}

// imprimir numeros pares
for(i=2; i<=10; i+=2){
    console.log(i);
}

// imprimir numeros pares con if
for(i=1; i<=10; i++){
    if( i % 2 == 0){
        console.log(i);
    }
}